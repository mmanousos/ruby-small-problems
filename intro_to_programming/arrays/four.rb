# What does each method return in the following example?

arr = [15, 7, 18, 5, 12, 8, 5, 1]

1. arr.index(5)
# returns 3 (index of the first time that value '5' is present in the array)
2. arr.index[5]
# returns NoMethodError (is an invalid syntax)
3. arr[5]
# returns 8 (value in the index position '5')
